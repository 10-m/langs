#!env python3
# -*- coding: utf-8 -*-

import math

# ceil
print(math.ceil(2.6))

# floor
print(math.floor(2.6))

# int
print(int(3.2))

# round
print(round(3.1415, 2))

# abs
print(abs(-3))

# pow
print(math.pow(2, 3))
print(2 ** 3)

# sqrt
print(math.sqrt(25))

# hypot
print(math.hypot(1, 2))
print(math.sqrt(1 ** 2 + 2 ** 2))

# radians
print(math.radians(90))

# degrees
print(math.degrees(3.1415))

# sin, cos, tan, atan2
print(math.sin(math.radians(90)))
print(math.cos(math.radians(90)))
print(math.tan(math.radians(45)))
print(math.degrees(math.atan2(-1, 1)))
